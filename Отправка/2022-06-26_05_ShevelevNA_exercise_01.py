"""Функция для получения случайного слова из списка через функцию random"""

def get_word(words_list):
    word_random_list = [] # создаем новый список для перемешанного списка
    word_random_list_temp = words_list
    #random.shuffle(word_random_list_temp) # Применяем функцию перемешавания списка
    random.choice(word_random_list_temp) # Применяем функцию перемешавания списка


    """ через цикл подставляю перемашанные слова в новый список , с проверкой чтоб не повторялись """
    for random_word in word_random_list_temp:
        if random_word not in word_random_list: #проверяем чтоб слова не повторялись в списке
            word_random_list.append(random_word)
        else:
            pass
    return word_random_list # возвращаем значение функии перемашанный список


"""Функция для подсчета статистики"""
