"""Функция, которая переводит слова на английском языке в последовательности точек и тирe."""
def morse_encode(any_word):  #значение аргумента может быть любое это просто указывается зависимость
    list_word_keys = []  # спосок куда добавим перевод символов в морзе от ввода пользователя

    """создаем цикл для разбития по символам ввода пользователя т.е. делаем спсиок по символам"""
    for letter_word in any_word:
        list_word_keys.append(alphabet[letter_word]) # обращаемся к словарю и в качестве индекса используем символ из воода пользователя
    return " ".join(list_word_keys) # возвращаем функции обьединеный список через пробел в морзе



